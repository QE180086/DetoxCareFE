// Function to get featured blogs
export const getFeaturedBlogs = () => {
  return allBlogs.filter(blog => blog.featured === true);
};

// Function to get blog by ID
export const getBlogById = (id) => {
  return allBlogs.find(blog => blog.id === parseInt(id));
};

// Function to get blogs by category
export const getBlogsByCategory = (category) => {
  if (category === 'all') return allBlogs;
  return allBlogs.filter(blog => blog.category === category);
};